package by.tolkach.mail.dto;

public class MailParamWrapper {

    private Mail mail;
    private Param param;

    public MailParamWrapper() {
    }

    public Mail getMail() {
        return mail;
    }

    public void setMail(Mail mail) {
        this.mail = mail;
    }

    public Param getParam() {
        return param;
    }

    public void setParam(Param param) {
        this.param = param;
    }
}
